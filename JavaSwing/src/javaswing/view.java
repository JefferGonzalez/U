    private void jButtonCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalculateActionPerformed

        if (jCheckBoxAdd.isSelected() && (jCheckBoxSubtract.isSelected() || jCheckBoxMultiply.isSelected() || jCheckBoxDivide.isSelected())
                || jCheckBoxSubtract.isSelected() && (jCheckBoxAdd.isSelected() || jCheckBoxMultiply.isSelected() || jCheckBoxDivide.isSelected())
                || jCheckBoxMultiply.isSelected() && (jCheckBoxSubtract.isSelected() || jCheckBoxAdd.isSelected() || jCheckBoxDivide.isSelected())
                || jCheckBoxDivide.isSelected() && (jCheckBoxSubtract.isSelected() || jCheckBoxMultiply.isSelected() || jCheckBoxAdd.isSelected())) {
            JOptionPane.showMessageDialog(null, "Seleccione solo un elemento por favor", "Información", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        Calculadora calculadora = new Calculadora();

        double NumberOne = Double.parseDouble(jTextFieldNumberOne.getText());
        double NumberTwo = Double.parseDouble(jTextFieldNumberTwo.getText());

        calculadora.setNumberOne(NumberOne);
        calculadora.setNumberTwo(NumberTwo);

        if (jCheckBoxAdd.isSelected()) {
            calculadora.add();
        }
        if (jCheckBoxSubtract.isSelected()) {
            calculadora.subtract();
        }
        if (jCheckBoxMultiply.isSelected()) {
            calculadora.multiply();
        }
        if (jCheckBoxDivide.isSelected()) {
            calculadora.divide();
        }

        String result = String.valueOf(calculadora.getResult());

        jTextFieldResult.setText(result);

    }//GEN-LAST:event_jButtonCalculateActionPerformed

    private void jButtonGenerateFibonacciActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGenerateFibonacciActionPerformed
        // TODO add your handling code here:

        String text = jTextFieldN.getText();
        if (text.equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese un número.", "Información", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        int N = Integer.parseInt(text);

        if (N <= 0) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese números mayores a cero.", "Información", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        Fibonacci fibo = new Fibonacci();

        fibo.setLimit(N);

        fibo.generateSequence();

        ArrayList sequence = fibo.getSequence();

        for (int i = 0; i < sequence.size(); i++) {
            int position = i + 1;
            JOptionPane.showMessageDialog(null, "En la posición " + position + ": " + sequence.get(i), "Fibonnaci", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_jButtonGenerateFibonacciActionPerformed

