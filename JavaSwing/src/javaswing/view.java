    private void jButtonGenerateTrapecioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGenerateTrapecioActionPerformed
        // TODO add your handling code here:
        String horizontal = jTextFieldHorizontalSize.getText();
        String vertical = jTextFieldVerticalSize.getText();
        if (horizontal.equals("") || vertical.equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor rellene los campos.", "Información", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        int H = Integer.parseInt(horizontal);
        int V = Integer.parseInt(vertical);

        if (H <= 0 || V <= 0) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese números mayores a cero.", "Información", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        Trapecio trapecio = new Trapecio();

        trapecio.setHorizontalSise(H);
        trapecio.setVerticalSize(V);

        trapecio.generateForma();

        String forma = trapecio.getForma();

        jTextAreaTrapecio.setText(forma);
    }//GEN-LAST:event_jButtonGenerateTrapecioActionPerformed

